import {mkdir, readFile, writeFile} from "node:fs/promises";
import {fileURLToPath} from "node:url";
import {dirname, resolve} from "node:path";
import Ajv2020 from "ajv/dist/2020.js";
import standaloneCode from "ajv/dist/standalone/index.js";

const packageRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const repositoryRoot = resolve(packageRoot, "../..");
const schemaPath = resolve(repositoryRoot, "spec/petrinet-contribution-ir.schema.json");
const outputPath = resolve(packageRoot, "src/dsl/ir/generated-validator.ts");
const check = process.argv.includes("--check");

const schema = JSON.parse(await readFile(schemaPath, "utf8"));
const ajv = new Ajv2020({
  allErrors: true,
  code: {esm: true, lines: true, optimize: true, source: true},
  strict: false,
  strictNumbers: true,
  validateFormats: false,
});
ajv.addSchema(schema);

let validatorSource = standaloneCode(ajv, {
  validateContributionIrWire: schema.$id,
});

const exportedDeclaration = "export const validateContributionIrWire =";
if (validatorSource.split(exportedDeclaration).length !== 2) {
  throw new Error("Ajv emitted an unexpected standalone validator declaration.");
}
validatorSource = validatorSource
  .replace(exportedDeclaration, "const validateContributionIrWire =")
  .replace(
    /const (func\d+) = require\("ajv\/dist\/runtime\/ucs2length"\)\.default;/g,
    [
      "function $1(value){",
      "let length = 0;",
      "for(let index = 0; index < value.length; index++){",
      "const code = value.charCodeAt(index);",
      "if(code >= 0xd800 && code <= 0xdbff && index + 1 < value.length){",
      "const next = value.charCodeAt(index + 1);",
      "if((next & 0xfc00) === 0xdc00) index++;",
      "}",
      "length++;",
      "}",
      "return length;",
      "}",
    ].join("\n"),
  );

if (/\brequire\s*\(|\b(?:new\s+)?Function\s*\(|(^|[^\w$.])eval\s*\(|["']node:/mu.test(validatorSource)) {
  throw new Error("Generated IR validator contains a browser/CSP-incompatible runtime token.");
}

const header = "// Generated by tools/generate-ir-schema.mjs. Do not edit.\n";
const publicApi = `
export interface ContributionIrValidationError {
  readonly instancePath: string;
  readonly schemaPath: string;
  readonly keyword: string;
  readonly params: Readonly<Record<string, unknown>>;
  readonly message?: string;
}

export type ContributionIrValidationResult =
  | {readonly ok: true}
  | {readonly ok: false; readonly errors: readonly ContributionIrValidationError[]};

export function validateContributionIr(value: unknown): ContributionIrValidationResult {
  if (validateContributionIrWire(value)) return {ok: true};
  return {
    ok: false,
    errors: (validateContributionIrWire.errors ?? []) as readonly ContributionIrValidationError[],
  };
}
`;
const output = `${header}// @ts-nocheck -- Ajv emits standalone JavaScript.\n${validatorSource}${publicApi}`;

if (check) {
  let actual;
  try {
    actual = await readFile(outputPath, "utf8");
  } catch {
    actual = undefined;
  }
  if (actual !== output) {
    console.error(
      `Generated IR validator is stale:\n- ${outputPath}\nRun pnpm run generate:ir-schema.`,
    );
    process.exitCode = 1;
  } else {
    console.log("Generated IR validator is current.");
  }
} else {
  await mkdir(dirname(outputPath), {recursive: true});
  await writeFile(outputPath, output);
  console.log("Generated IR validator updated.");
}
